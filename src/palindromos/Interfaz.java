    public boolean isPalindrome(Lista lista) throws Exception {
        if ( ! lista.estaVacia() ) {
            int size = lista.getTamaño(), halfSize = size / 2;
            for (int i = 0; i < halfSize; i++)
                if (lista.getNodo(i).getValor() != lista.getNodo(size - i - 1).getValor())
                    return false;
            return true;
        }
        throw new Exception("La secuencia no existe");
    }
