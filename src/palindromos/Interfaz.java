    private void botonImprimirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonImprimirMouseClicked
        if ( ! this.secuencia.estaVacia() ) {
            salidaSecuencia.setText(secuencia.toString());
        } else {
            salidaSecuencia.setText("Secuencia vacía...");
        }
    }//GEN-LAST:event_botonImprimirMouseClicked

    private void botonCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonCrearMouseClicked
        String input = this.entradaSecuencia.getText();
        this.secuencia = new Lista();
        for(int i = 0; i < input.length(); i++) {
            this.secuencia.insertar(new Nodo(input.charAt(i)));
        }
        salidaPalindromo.setText(DEFAULT_PALINDROME_OUTPUT);
        
    }//GEN-LAST:event_botonCrearMouseClicked

    private void botonPalindromoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonPalindromoMouseClicked
        try {
            if (isPalindrome(secuencia)) {
                salidaPalindromo.setText("Es palíndromo");
            } else {
                salidaPalindromo.setText("No es palíndromo");
            }
        } catch (Exception error) {
            salidaPalindromo.setText(error.getMessage());
        }
    }//GEN-LAST:event_botonPalindromoMouseClicked

